import java.util.*;

public class fever {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int number = sc.nextInt();
        if((number>=100) ) {
            System.out.println("You are suffering from fever.");
        } 
    
        else {
            System.out.println("You are fine.");
        }
    }
    
}
